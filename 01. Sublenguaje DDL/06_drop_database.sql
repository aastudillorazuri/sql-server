/*
La sentencia DROP DATABASE se utiliza para eliminar una base de datos existente en 
SQL.

Sintaxis:

DROP DATABASE nombredelabasededatos;

Nota: ¡Ten cuidado antes de eliminar una base de datos! Al borrar una base de datos, 
se perderá toda la información almacenada en ella.

La siguiente sentencia SQL elimina la base de datos existente llamada 'testDB':
*/

DROP DATABASE testDB;

/*
Nota: No es posible eliminar una base de datos mientras esté en uso. Antes de realizar 
esta acción, primero debes seleccionar otra base de datos, por ejemplo, master.
*/